const schemaNameOverrides = new Map([
  ["ApiError", "Jp1Ajs3WebApiError"],
  ["UnitListResponse", "Jp1Ajs3UnitListResponse"],
]);

function typeName(schemaName) {
  return schemaNameOverrides.get(schemaName) ?? `Jp1Ajs3${schemaName}`;
}

function refName(ref) {
  return ref.split("/").at(-1);
}

function resolveRef(ref) {
  return schemas[refName(ref)];
}

function normalizeSchema(schema) {
  if (schema?.$ref) {
    return resolveRef(schema.$ref);
  }

  if (schema?.allOf?.length === 1) {
    return normalizeSchema(schema.allOf[0]);
  }

  return schema;
}

function literal(value) {
  return JSON.stringify(value);
}

function typeForSchema(schema) {
  const normalized = normalizeSchema(schema);
  if (!normalized) {
    return "unknown";
  }

  if (schema?.$ref) {
    return typeName(refName(schema.$ref));
  }

  if (schema?.allOf?.length === 1 && schema.allOf[0].$ref) {
    return typeName(refName(schema.allOf[0].$ref));
  }

  if (normalized.enum) {
    return normalized.enum.map(literal).join(" | ");
  }

  switch (normalized.type) {
    case "array":
      return `ReadonlyArray<${typeForSchema(normalized.items)}>`;
    case "boolean":
      return "boolean";
    case "integer":
    case "number":
      return "number";
    case "object":
      return typeForObjectSchema(normalized);
    case "string":
      return "string";
    default:
      return "unknown";
  }
}

function typeForNullableSchema(schema) {
  const nullable = schema?.nullable === true;
  const type = typeForSchema(schema);
  return nullable ? `${type} | null` : type;
}

function typeForObjectSchema(schema) {
  if (!schema.properties) {
    return schema.additionalProperties === true
      ? "Record<string, unknown>"
      : "Record<string, never>";
  }

  const required = new Set(schema.required ?? []);
  const properties = Object.entries(schema.properties).map(
    ([name, property]) => {
      const optional = required.has(name) ? "" : "?";
      return `${name}${optional}: ${typeForNullableSchema(property)};`;
    },
  );

  if (schema.additionalProperties === true) {
    properties.push("[key: string]: unknown;");
  }

  return `{ ${properties.join(" ")} }`;
}

function parameterType(parameterRef) {
  const parameter = document.components.parameters[refName(parameterRef.$ref)];
  return {
    name: parameter.name,
    required: parameter.required === true,
    type: typeForSchema(parameter.schema),
  };
}

function renderInterface(name, schema) {
  const normalized = normalizeSchema(schema);
  if (normalized.type !== "object") {
    return `export type ${name} = ${typeForSchema(normalized)};\n`;
  }

  const required = new Set(normalized.required ?? []);
  const lines = [`export interface ${name} {`];
  for (const [propertyName, propertySchema] of Object.entries(
    normalized.properties ?? {},
  )) {
    const optional = required.has(propertyName) ? "" : "?";
    lines.push(
      `  ${propertyName}${optional}: ${typeForNullableSchema(propertySchema)};`,
    );
  }

  if (normalized.additionalProperties === true) {
    lines.push("  [key: string]: unknown;");
  }

  lines.push("}");
  return `${lines.join("\n")}\n`;
}

function renderRequestInterface() {
  const params = getUnitList.parameters.map(parameterType);
  const lines = [
    "export interface Jp1Ajs3GetUnitListRequest {",
    "  readonly headers?: {",
  ];

  for (const param of params.filter(
    (param) => param.name === "Accept-Language",
  )) {
    lines.push(`    readonly ${literal(param.name)}?: ${param.type};`);
  }

  lines.push("  };", "  readonly query: {");

  for (const param of params.filter(
    (param) => param.name !== "Accept-Language",
  )) {
    const optional = param.required ? "" : "?";
    lines.push(`    readonly ${param.name}${optional}: ${param.type};`);
  }

  lines.push("  };", "}");
  return `${lines.join("\n")}\n`;
}

